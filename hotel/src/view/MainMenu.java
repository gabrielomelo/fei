    private Integer getReserveId() {
        int index = this.jTable1.getSelectedRow();
        if(index != -1)
            return Integer.parseInt(this.jTable1.getModel().getValueAt(index, 0).toString());
        return index;
    }
    
    private Integer getReserveRoom() {
        int index = this.jTable1.getSelectedRow();
        if(index != -1)
            return Integer.parseInt(this.jTable1.getModel().getValueAt(index, 1).toString());
        return index;
    }
    
    // reload all the items
    private Object[][] reloadTable() {
        return this.reloadTable(this.resDAO.getAllReserves());
    }
    
    // reload only the given values
    private Object[][] reloadTable(Comparable[][] c) {
        String[][] temp = new String[this.reservas.getSize()][];
        int index = 0;
        for (int i = 0; i < c.length; i++) {
            for(int j = 0; j < c[i].length; j++){
                Reserve reserva = (Reserve) c[i][j];
                temp[index] = new String[] {
                        ""+reserva.getId(), ""+reserva.getRoom(), reserva.getTitular().getName(),
                        reserva.getTitular().getCPF(), reserva.getInitDate(), reserva.getCheckinHour(),
                        reserva.getEndDate(), reserva.getCheckoutHour()
                    };
                index++;
            }
        }
        return temp;
    }
    
