    private boolean hasChar(String stream) {
        for (int i = 0; i < stream.length(); i++)
            if(!Character.isDigit(stream.charAt(i)))
                return true;        
        return false;
    }
    
    private void searchReserveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchReserveButtonActionPerformed
        String searchedRoom = this.nomeReserva.getText();
        if(searchedRoom.isEmpty() || this.hasChar(searchedRoom)) return;
        Object[][] data = this.reloadTable(
                new Comparable[][] {this.resDAO.getReservesByRoom(Integer.parseInt(searchedRoom))}
        );
        this.jTable1.setModel(this.getModel(data));
    }//GEN-LAST:event_searchReserveButtonActionPerformed

    private DefaultTableModel getModel(Object[][] data) {
        return new DefaultTableModel(
            data,
            new String [] {
            "ID", "Quarto", "Titular", "CPF", "Dta. Inicio",
            "Hr. CheckIn", "Dta. Final", "Hr. CheckOut"
            });
    }
    
